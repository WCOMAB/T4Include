                string match,
                out int depth,
                out TValue value
                )
            {
                match = match ?? "";
                depth = 0;
                value = default(TValue);
                var length = match.Length;
                var node = m_root;
    
                Node currentBestMatch = null;
    
                for (var iter = 0; iter < length; ++iter)
                {
                    var ch = match[iter];
    
                    var edge = node.FindEdge(ch);
                    if (edge == null)
                    {
                        if (currentBestMatch != null)
