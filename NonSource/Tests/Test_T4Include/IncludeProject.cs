            public bool AddOrReplace (string key, TValue value)
            {
                key = key ?? "";
                var length = key.Length;
                var node = m_root;
                for (var iter = 0 ; iter < length; ++iter)
                {
                    var ch = key[iter];
    
                    var edge = node.FindEdge(ch);
                    if (edge == null)
                    {
                        edge = node.AddEdge(ch);
                    }
                    node = edge;
                }
    
                var replaced = !node.HasValue;
    
                node.Value = value;
                node.HasValue = true;
    
                return replaced;
            }
    
