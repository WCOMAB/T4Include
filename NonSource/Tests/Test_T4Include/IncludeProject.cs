        enum ExitCode
        {
            Ok                  = 0         ,
            InvalidConfigFile   = 101       ,
            Unknown             = 999       ,
        }
    
        sealed partial class ExitCodeException : Exception
        {
            public readonly ExitCode ExitCode;
    
            public ExitCodeException(ExitCode exitCode)
