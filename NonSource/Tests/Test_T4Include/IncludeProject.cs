                            sExpected.ToString(0, 16),
                            sFound.ToString(0, 16),
                            message
                            );
                        return true;
                    }
    
                }
    
                var finalMessage = "TestFor.Equality: #EXPECTED:{0}, #FOUND:{1} - {2}"
                    .FormatWith(
